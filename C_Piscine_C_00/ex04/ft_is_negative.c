#include <unistd.h>

void	ft_is_negative(int n);

void	ft_is_negative(int n)
{
	char	c;

	if (n < 0)
	{
		c = 'N';
	}
	if (n >= 0)
	{
		c = 'P';
	}
	write(1, &c, 1);
}
