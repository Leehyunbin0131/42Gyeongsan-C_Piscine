#include <unistd.h>

void	ft_putnbr(int nb);

void	ft_putnbr(int nb)
{
	long long		k;
	char			c;

	k = nb;
	if (k < 0)
	{
		write(1, "-", 1);
		k = k * -1;
	}
	if (k >= 10)
	{
		ft_putnbr(k / 10);
	}
	c = (k % 10) + 48;
	write(1, &c, 1);
}
